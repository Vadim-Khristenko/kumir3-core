//! Токены (лексемы) языка Кумир 2/3
//! 
//! Kumir 3 расширяет классический Кумир:
//! - Модульная система (подключить, модуль, экспорт)
//! - Указатели и ручное управление памятью
//! - Перечисления с pattern matching
//! - Автоматический вывод типов (авто)
//! - Rust-вставки
//! - Лямбда-выражения
//! - Асинхронное программирование
//! - Обработка ошибок

/// Токен (лексема) языка Кумир 2/3.
#[derive(Debug, Clone, PartialEq)]
pub enum Token {
    // =========================================================================
    // Структура алгоритма (Kumir 2)
    // =========================================================================
    Alg,            // алг      — начало алгоритма
    Begin,          // нач      — начало тела алгоритма
    End,            // кон      — конец алгоритма
    Given,          // дано     — предусловие
    Need,           // надо     — постусловие
    Arg,            // арг      — входной аргумент
    Res,            // рез      — выходной результат
    ArgRes,         // аргрез   — входной и выходной аргумент
    
    // =========================================================================
    // Базовые типы (Kumir 2)
    // =========================================================================
    IntType,         // цел      — целое число
    FloatType,       // вещ      — вещественное число
    BoolType,        // лог      — логическое значение
    CharType,        // сим      — символ
    StringType,      // лит      — литерал (строка)
    ArrayType,       // таб      — таблица (массив)
    
    // =========================================================================
    // Расширенные типы (Kumir 3)
    // =========================================================================
    PointerType,    // указатель    — указатель на значение/ячейку памяти
    EnumType,       // перечисление — тип-перечисление (enum)
    AutoType,       // авто         — автоматический вывод типа
    NoneType,       // пустота      — тип отсутствует (void)
    OptionalType,   // необязательно — опциональный тип (может быть None)
    
    // =========================================================================
    // Логические операторы и константы
    // =========================================================================
    And,            // и        — логическое И
    Or,             // или      — логическое ИЛИ
    Not,            // не       — логическое НЕ
    True,           // да       — истина
    False,          // нет      — ложь
    
    // =========================================================================
    // Управление потоком (Kumir 2)
    // =========================================================================
    If,             // если     — условный оператор
    Then,           // то       — начало ветки условия
    Else,           // иначе    — альтернативная ветка
    Fi,             // все      — конец условного оператора
    Switch,         // выбор    — оператор выбора
    Case,           // при      — ветка выбора
    Loop,           // нц       — начало цикла
    EndLoop,        // кц       — конец цикла
    For,            // для      — цикл с параметром
    From,           // от       — начальное значение
    To,             // до       — конечное значение
    Step,           // шаг      — шаг цикла
    While,          // пока     — цикл с условием
    
    // =========================================================================
    // Ввод/вывод и управление (Kumir 2)
    // =========================================================================
    Input,          // ввод     — ввод данных
    Output,         // вывод    — вывод данных
    Assert,         // утв      — утверждение (assertion)
    Pause,          // пауза    — приостановка выполнения
    Halt,           // выход    — завершение программы
    Use,            // использовать — подключение исполнителя
    Return,         // вернуть  — возврат значения из алгоритма
    ResultValue,    // знач     — возвращаемое значение алгоритма
    
    // =========================================================================
    // Модули и импорт (Kumir 3)
    // =========================================================================
    Import,         // подключить   — подключение библиотек и .kum файлов
    Module,         // модуль       — объявление модуля
    Export,         // экспорт      — экспорт из модуля
    
    // =========================================================================
    // Работа с памятью и указателями (Kumir 3)
    // =========================================================================
    New,            // новый        — создание нового объекта/указателя
    Delete,         // удалить      — освобождение памяти
    Ref,            // ссылка       — получение ссылки на значение
    Deref,          // разыменовать — разыменование указателя
    
    // =========================================================================
    // Перечисления и pattern matching (Kumir 3)
    // =========================================================================
    EnumDecl,       // перечисление — объявление enum
    Match,          // совпадение   — pattern matching
    
    // =========================================================================
    // Классы и ООП (Kumir 3)
    // =========================================================================
    Class,          // класс        — объявление класса
    Struct,         // структура    — объявление структуры (данные без методов)
    Interface,      // интерфейс    — объявление интерфейса
    Trait,          // свойство     — объявление trait (как в Rust)
    Impl,           // реализация   — реализация методов для типа
    Self_,          // я / себя     — ссылка на текущий объект
    This,           // это / this   — альтернативное ключевое слово для Self_
    Super,          // предок       — ссылка на родительский класс
    Constructor,    // конструктор  — метод инициализации
    Destructor,     // деструктор   — метод финализации
    Public,         // открытый     — публичный доступ
    Private,        // закрытый     — приватный доступ
    Protected,      // защищённый   — защищённый доступ
    Static,         // статический  — статический член
    Virtual,        // виртуальный  — виртуальный метод (для переопределения)
    Override,       // переопределить — переопределение метода
    Abstract,       // абстрактный  — абстрактный метод/класс
    Final,          // финальный    — запрет наследования/переопределения
    Extends,        // расширяет    — наследование
    Implements,     // реализует    — реализация интерфейса
    
    // =========================================================================
    // Rust-вставки (Kumir 3)
    // =========================================================================
    RustBlockStart, // РастВставкаНЦ — начало блока Rust-кода
    RustBlockEnd,   // РастВставкаКЦ — конец блока Rust-кода
    RustInline(String), // содержимое Rust-вставки
    Rust,           // ржавчина   — альтернативный синтаксис
    
    // =========================================================================
    // Функциональное программирование (Kumir 3)
    // =========================================================================
    Lambda,         // лямбда       — анонимная функция
    Pipe,           // |>           — оператор pipe (конвейер)
    Compose,        // >>           — композиция функций
    
    // =========================================================================
    // Асинхронное программирование (Kumir 3)
    // =========================================================================
    Async,          // асинх        — асинхронный алгоритм
    Await,          // ждать        — ожидание результата
    
    // =========================================================================
    // Обработка ошибок (Kumir 3)
    // =========================================================================
    Try,            // попытка      — начало блока обработки ошибок
    Catch,          // перехват     — обработка исключения
    Throw,          // бросить      — генерация исключения
    Finally,        // наконец      — блок завершения
    
    // =========================================================================
    // Специальные значения (Kumir 3)
    // =========================================================================
    None,           // Пусто        — отсутствие значения (None)
    NotImplemented, // НеРеализовано — заглушка для нереализованных методов
    
    // =========================================================================
    // Идентификаторы и литералы
    // =========================================================================
    Identifier(String),         // имя переменной/функции
    Integer(i64),               // целочисленный литерал
    Float(f64),                 // вещественный литерал
    String(String),             // строковый литерал
    Char(char),                 // символьный литерал
    
    // =========================================================================
    // Rust-вставки - содержимое
    // =========================================================================
    RustCode(String),           // содержимое Rust-блока
    
    // =========================================================================
    // Арифметические операторы
    // =========================================================================
    Plus,           // +        — сложение
    Minus,          // -        — вычитание
    Star,           // *        — умножение
    Slash,          // /        — деление
    Percent,        // %        — остаток от деления (mod)
    Power,          // **       — возведение в степень
    
    // =========================================================================
    // Операторы сравнения
    // =========================================================================
    Equal,          // =        — равенство
    NotEqual,       // <>       — неравенство
    Less,           // <        — меньше
    Greater,        // >        — больше
    LessEqual,      // <=       — меньше или равно
    GreaterEqual,   // >=       — больше или равно
    
    // =========================================================================
    // Операторы присваивания
    // =========================================================================
    Assign,         // :=       — присваивание
    PlusAssign,     // +=       — сложение с присваиванием
    MinusAssign,    // -=       — вычитание с присваиванием
    StarAssign,     // *=       — умножение с присваиванием
    SlashAssign,    // /=       — деление с присваиванием
    
    // =========================================================================
    // Разделители и скобки
    // =========================================================================
    LParen,         // (        — левая круглая скобка
    RParen,         // )        — правая круглая скобка
    LBracket,       // [        — левая квадратная скобка
    RBracket,       // ]        — правая квадратная скобка
    LBrace,         // {        — левая фигурная скобка
    RBrace,         // }        — правая фигурная скобка
    Comma,          // ,        — запятая
    Colon,          // :        — двоеточие
    SemiColon,      // ;        — точка с запятой
    Dot,            // .        — точка
    DoubleDot,      // ..       — диапазон
    Ellipsis,       // ...      — многоточие (varargs)
    
    // =========================================================================
    // Специальные операторы (Kumir 3)
    // =========================================================================
    Arrow,          // ->       — стрелка (для лямбд и указателей)
    FatArrow,       // =>       — толстая стрелка (для match)
    DoubleColon,    // ::       — доступ к модулю/enum
    At,             // @        — декоратор/аннотация
    Ampersand,      // &        — ссылка
    Caret,          // ^        — разыменование
    Question,       // ?        — оператор ? (ранний возврат ошибки)
    
    // =========================================================================
    // Специальные токены
    // =========================================================================
    Newline,        // перевод строки (значимый в Кумире)
    Comment(String),// комментарий
    DocComment(String), // документирующий комментарий (///)
    EOF,            // конец файла
}

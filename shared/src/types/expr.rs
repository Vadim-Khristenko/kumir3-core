//! Выражения (Expressions) языка Кумир
//! 
//! Выражения вычисляют значения и могут быть вложенными.

use super::value::Value;
use super::token::Token;
use super::type_spec::TypeSpec;
use super::pattern::Pattern;

/// Выражение в языке Кумир.
#[derive(Debug, Clone, PartialEq)]
pub enum Expr {
    // -------------------------------------------------------------------------
    // Базовые выражения (Kumir 2)
    // -------------------------------------------------------------------------
    Literal(Value),                         // литерал (число, строка, ...)
    Variable(String),                       // переменная
    BinaryOp(Box<Expr>, Token, Box<Expr>),  // бинарная операция: a + b
    UnaryOp(Token, Box<Expr>),              // унарная операция: -x, не x
    Call(String, Vec<Expr>),                // вызов алгоритма: foo(x, y)
    ArrayAccess(String, Vec<Expr>),         // доступ к элементу: arr[i, j]
    
    // -------------------------------------------------------------------------
    // ООП выражения (Kumir 3)
    // -------------------------------------------------------------------------
    /// Доступ к полю объекта: объект.поле
    FieldAccess(Box<Expr>, String),
    
    /// Вызов метода: объект.метод(аргументы)
    MethodCall {
        object: Box<Expr>,
        method: String,
        args: Vec<Expr>,
    },
    
    /// Создание экземпляра класса: новый Класс(аргументы)
    /// 
    /// Семантика ключевого слова `новый`:
    /// - `новый Класс()` — создаёт новый экземпляр класса
    /// - При множественном присваивании `a, b := новый X()` — каждая переменная получает отдельный объект
    /// - Без `новый`: `a, b := Класс.Метод()` — все переменные ссылаются на один объект
    NewInstance {
        class_name: String,
        args: Vec<Expr>,
    },
    
    /// Ссылка на себя: я / self
    SelfRef,
    
    /// Ссылка на предка: предок / super
    SuperRef,
    
    /// Приведение типа: x как Тип
    Cast {
        expr: Box<Expr>,
        target_type: TypeSpec,
    },
    
    /// Проверка типа: x это Тип
    TypeCheck {
        expr: Box<Expr>,
        check_type: TypeSpec,
    },
    
    // -------------------------------------------------------------------------
    // Модули и перечисления (Kumir 3)
    // -------------------------------------------------------------------------
    /// Доступ к модулю: Модуль::функция
    ModuleAccess(String, String),
    
    /// Создание значения перечисления: Цвет::Красный или Опция::Некоторое(42)
    EnumConstruct {
        enum_name: String,
        variant: String,
        data: Option<Box<Expr>>,
    },
    
    // -------------------------------------------------------------------------
    // Указатели (Kumir 3)
    // -------------------------------------------------------------------------
    /// Получение ссылки: &x
    Ref(Box<Expr>),
    
    /// Разыменование указателя: ^ptr или *ptr
    Deref(Box<Expr>),
    
    /// Создание нового указателя: новый цел(42)
    New(Box<Expr>),
    
    // -------------------------------------------------------------------------
    // Функциональное программирование (Kumir 3)
    // -------------------------------------------------------------------------
    /// Лямбда-выражение: лямбда(x, y) -> x + y
    Lambda {
        params: Vec<String>,
        body: Box<Expr>,
    },
    
    /// Pipe-выражение: x |> f |> g  (эквивалентно g(f(x)))
    Pipe(Box<Expr>, Box<Expr>),
    
    // -------------------------------------------------------------------------
    // Условные выражения (Kumir 3)
    // -------------------------------------------------------------------------
    /// Тернарный оператор: если x > 0 то x иначе -x все
    IfExpr {
        condition: Box<Expr>,
        then_expr: Box<Expr>,
        else_expr: Box<Expr>,
    },
    
    /// Match-выражение (возвращает значение)
    MatchExpr {
        expr: Box<Expr>,
        arms: Vec<(Pattern, Expr)>,
    },
    
    // -------------------------------------------------------------------------
    // Rust-вставки (Kumir 3)
    // -------------------------------------------------------------------------
    /// Inline Rust-выражение, возвращающее значение
    RustExpr(String),
    
    // -------------------------------------------------------------------------
    // Специальные выражения (Kumir 3)
    // -------------------------------------------------------------------------
    /// Пусто / None — отсутствие значения
    None,
    
    /// НеРеализовано — заглушка для нереализованных методов
    /// При вычислении бросает ошибку "Пока что не реализовано: {название}"
    NotImplemented(Option<String>),
}

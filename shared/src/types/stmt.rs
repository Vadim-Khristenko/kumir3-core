//! Инструкции (Statements) языка Кумир
//! 
//! Инструкции выполняют действия и не возвращают значений напрямую.

use super::expr::Expr;
use super::type_spec::TypeSpec;
use super::pattern::Pattern;
use super::class::{ClassDef, Field, Method, MethodSignature};

/// Вариант перечисления (enum variant).
#[derive(Debug, Clone, PartialEq)]
pub struct EnumVariant {
    pub name: String,               // имя варианта
    pub data: Option<TypeSpec>,     // тип ассоциированных данных (если есть)
}

/// Ветка match-выражения.
#[derive(Debug, Clone, PartialEq)]
pub struct MatchArm {
    pub pattern: Pattern,           // паттерн для сопоставления
    pub guard: Option<Expr>,        // дополнительное условие (при ... если ...)
    pub body: Vec<Stmt>,            // тело ветки
}

/// Инструкция (оператор) языка Кумир.
#[derive(Debug, Clone, PartialEq)]
pub enum Stmt {
    // -------------------------------------------------------------------------
    // Присваивание (Kumir 2)
    // -------------------------------------------------------------------------
    Assignment(String, Expr),               // x := выражение
    ArrayAssignment(String, Vec<Expr>, Expr), // arr[i, j] := выражение
    
    // -------------------------------------------------------------------------
    // Условные операторы (Kumir 2)
    // -------------------------------------------------------------------------
    If {
        condition: Expr,
        then_branch: Vec<Stmt>,
        else_branch: Option<Vec<Stmt>>,
    },
    
    // -------------------------------------------------------------------------
    // Циклы (Kumir 2)
    // -------------------------------------------------------------------------
    LoopWhile {
        condition: Expr,
        body: Vec<Stmt>,
    },
    LoopFor {
        variable: String,
        from: Expr,
        to: Expr,
        step: Option<Expr>,
        body: Vec<Stmt>,
    },
    /// Бесконечный цикл (нц ... кц)
    LoopInfinite {
        body: Vec<Stmt>,
    },
    /// Цикл с постусловием (нц ... кц_при условие)
    LoopDoWhile {
        body: Vec<Stmt>,
        condition: Expr,
    },
    
    // -------------------------------------------------------------------------
    // Ввод/вывод (Kumir 2)
    // -------------------------------------------------------------------------
    Input(Vec<String>),                     // ввод x, y, z
    Output(Vec<Expr>),                      // вывод a, b, c
    
    // -------------------------------------------------------------------------
    // Управление потоком (Kumir 2)
    // -------------------------------------------------------------------------
    Assert(Expr),                           // утв условие
    ExprStmt(Expr),                         // выражение как инструкция
    Return,                                 // выход из алгоритма (без значения)
    ReturnValue(Expr),                      // вернуть выражение
    ResultAssign(Expr),                     // знач := выражение
    Break,                                  // выход из цикла
    Continue,                               // следующая итерация цикла
    
    // -------------------------------------------------------------------------
    // Объявление переменных (Kumir 3)
    // -------------------------------------------------------------------------
    /// Объявление с автоматическим выводом типа: авто x := 42
    AutoVarDecl { name: String, init: Expr },
    
    /// Объявление с явным типом: цел x или цел x, y, z или цел x := 42
    VarDecl {
        type_spec: TypeSpec,
        names: Vec<String>,
        init: Option<Expr>,  // Инициализация применяется только если одна переменная
    },
    
    // -------------------------------------------------------------------------
    // Модули и импорт (Kumir 3)
    // -------------------------------------------------------------------------
    /// Подключение модуля/файла: подключить "file.kum" или подключить Модуль
    Import { path: String, alias: Option<String> },
    
    /// Объявление модуля
    ModuleDecl { 
        name: String, 
        body: Vec<Stmt>,
        algorithms: Vec<crate::types::Algorithm>,
    },
    
    /// Экспорт из модуля
    Export { names: Vec<String> },
    
    // -------------------------------------------------------------------------
    // Перечисления (Kumir 3)
    // -------------------------------------------------------------------------
    /// Объявление перечисления
    EnumDecl {
        name: String,
        variants: Vec<EnumVariant>,
    },
    
    /// Pattern matching
    Match {
        expr: Expr,
        arms: Vec<MatchArm>,
    },
    
    // -------------------------------------------------------------------------
    // Указатели (Kumir 3)
    // -------------------------------------------------------------------------
    /// Создание указателя: новый x := 42
    PointerNew { name: String, value: Expr },
    
    /// Освобождение памяти: удалить x
    PointerDelete { name: String },
    
    // -------------------------------------------------------------------------
    // Обработка ошибок (Kumir 3)
    // -------------------------------------------------------------------------
    /// Блок try-catch-finally
    TryCatch {
        try_block: Vec<Stmt>,
        catch_var: Option<String>,
        catch_block: Vec<Stmt>,
        finally_block: Option<Vec<Stmt>>,
    },
    
    /// Генерация исключения: бросить "сообщение"
    Throw(Expr),
    
    // -------------------------------------------------------------------------
    // Rust-вставки (Kumir 3)
    // -------------------------------------------------------------------------
    /// Блок Rust-кода: РастВставкаНЦ ... РастВставкаКЦ
    RustBlock {
        code: String,
        /// Переменные Кумира, доступные в Rust-блоке
        captured_vars: Vec<String>,
    },
    
    // -------------------------------------------------------------------------
    // Асинхронное программирование (Kumir 3)
    // -------------------------------------------------------------------------
    /// Ожидание асинхронного результата: ждать выражение
    Await(Expr),
    
    // -------------------------------------------------------------------------
    // Классы и ООП (Kumir 3)
    // -------------------------------------------------------------------------
    /// Объявление класса
    ClassDecl(ClassDef),
    
    /// Объявление структуры (только данные, без методов)
    StructDecl {
        name: String,
        fields: Vec<Field>,
    },
    
    /// Объявление интерфейса
    InterfaceDecl {
        name: String,
        methods: Vec<MethodSignature>,
    },
    
    /// Объявление trait (свойства)
    TraitDecl {
        name: String,
        methods: Vec<Method>,
    },
    
    /// Реализация trait для типа: реализация Trait для Тип
    ImplBlock {
        trait_name: Option<String>,
        target_type: String,
        methods: Vec<Method>,
    },
    
    /// Присваивание полю: объект.поле := значение
    FieldAssignment {
        object: Expr,
        field: String,
        value: Expr,
    },
}
